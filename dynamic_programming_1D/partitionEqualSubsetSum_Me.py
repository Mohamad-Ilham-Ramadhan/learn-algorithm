def canPartition(nums):
	total = sum(nums)
	if total % 2: return False
	nums.sort()
	half = total / 2
	dp = set([0])
	for i in range(len(nums)-1, -1, -1):
		if nums[i] > half: 
			continue 
		if nums[i] == half: 
			return True 
		newDpEls = []
		for t in dp: 
			if t + nums[i] == half:
				return True
			if t + nums[i] > half: continue
			if t + nums[i] not in dp: 
				newDpEls.append(t + nums[i])
		for el in newDpEls:
			dp.add(el)

	return False
